// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
 var charCount = 0;
 for ( let i = 0; i < string.length; i++) {
  if(string[i] === char){
     charCount++
  }
 }
 return charCount++
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
var bCount = 0;
for (let i = 0; i < string.length; i++){
  if(string[i] === 'B'){
    bCount ++;
  }
}
return bCount;
}

